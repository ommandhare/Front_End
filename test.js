// Function to perform arithmetic operations
function calculate(a, b) {
    console.log(`Adding ${a} + ${b}: ${a + b}`);
    console.log(`Subtracting ${a} - ${b}: ${a - b}`);
    console.log(`Multiplying ${a} * ${b}: ${a * b}`);
    console.log(`Dividing ${a} / ${b}: ${a / b}`);
}

// Example usage
let num1 = 10;
let num2 = 5;

console.log("Performing arithmetic operations on num1 and num2:");
calculate(num1, num2);

// Printing a custom message
console.log("Arithmetic operations completed successfully.");
